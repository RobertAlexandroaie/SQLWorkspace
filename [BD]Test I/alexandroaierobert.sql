SELECT E.NUME AS "ELEV", E.AN AS "AN", N.NOTA AS "NOTA", P.NUME AS "PROFESOR"
FROM ELEVI E, NOTE_ELEVI N, PROFESORI P
WHERE (E.MATRICOLA=N.MAT AND N.COD=P.COD)
;

--2
SELECT TRIM(NUME) AS PROFESOR
FROM PROFESORI
WHERE COD =	
	(
		SELECT COD
		FROM NOTE_ELEVI
		WHERE NOTA = 
			(
				SELECT MIN(NOTA)
				FROM NOTE_ELEVI
			)
	)
;

--3

SELECT N.NOTA,E.NUME
FROM ELEVI E, NOTE_ELEVI N
WHERE N.MAT=E.MATRICOLA 
;

--4
SELECT NUME
FROM ELEVI
WHERE MATRICOLA NOT IN
	(
		SELECT MAT
		FROM NOTE_ELEVI
	)
;

--5
SELECT NUME
FROM PROFESORI
WHERE COD IN
	(
		SELECT COD
		FROM NOTE_ELEVI
		WHERE MAT IN
			(
				SELECT MATRICOLA
				FROM ELEVI
				WHERE AN=2
			)
	)
;